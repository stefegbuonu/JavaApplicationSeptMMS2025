public class EscapeCharacter{
	public static void main(String[] args){
		System.out.print("When you need to use the escape characters \n");
		System.out.print("Use it when you want to manipulate your cursor \n");
		System.out.print("Use \\n when you want a new line \n");
		System.out.print("Who is the father of Java? The father of Java is \"James Gosling\"\n");
		System.out.print("\tUse \\t when you want a tab or indentation\r\n");
		System.out.print("We are using carriage return here");
	}
}